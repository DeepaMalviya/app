package online.masterji.honchiSolution.domain;

public class Userdetails {
  String algolia_app_id;
  String algolia_api_key;
  String algolia_index;

    public String getAlgolia_app_id() {
        return algolia_app_id;
    }

    public void setAlgolia_app_id(String algolia_app_id) {
        this.algolia_app_id = algolia_app_id;
    }

    public String getAlgolia_api_key() {
        return algolia_api_key;
    }

    public void setAlgolia_api_key(String algolia_api_key) {
        this.algolia_api_key = algolia_api_key;
    }

    public String getAlgolia_index() {
        return algolia_index;
    }

    public void setAlgolia_index(String algolia_index) {
        this.algolia_index = algolia_index;
    }
}
